function isLocale(value: string): value is Locale {
  return locales.includes(value as Locale)
}

function matchLocale(value?: string | null): Locale | null {
  if (!value) return null
  const normalized = value.replace('_', '-').toLowerCase()
  if (normalized === 'zh' || normalized === 'zh-cn' || normalized === 'zh-hans' || normalized.startsWith('zh-hans-')) return 'zh-CN'
  if (normalized === 'zh-tw' || normalized === 'zh-hk' || normalized === 'zh-mo' || normalized === 'zh-hant' || normalized.startsWith('zh-hant-')) return 'zh-TW'
  const language = normalized.split('-')[0]
  const match = locales.find((locale) => locale.toLowerCase() === normalized || locale.toLowerCase().split('-')[0] === language)
  return match ?? null
}

export function resolveLocale(value?: string | null): Locale {
  return matchLocale(value) ?? defaultLocale
}

function parseCookie(header: string | null, name: string) {
  if (!header) return null
  const cookies = header.split(';').map((part) => part.trim())
  const prefix = `${name}=`
  const cookie = cookies.find((part) => part.startsWith(prefix))
  return cookie ? decodeURIComponent(cookie.slice(prefix.length)) : null
}

function localeFromAcceptLanguage(header: string | null): Locale {
  if (!header) return defaultLocale
  const candidates = header
    .split(',')
    .map((entry) => {
      const [tag, qPart] = entry.trim().split(';q=')
      return { tag, q: qPart ? Number.parseFloat(qPart) : 1 }
    })
    .filter((entry) => entry.tag)
    .sort((a, b) => b.q - a.q)

  for (const candidate of candidates) {
    const locale = matchLocale(candidate.tag)
    if (locale) return locale
  }
  return defaultLocale
}

export function getRequestLocale(request: Request): Locale {
  const cookieLocale = parseCookie(request.headers.get('cookie'), localeCookie)
  if (cookieLocale && isLocale(cookieLocale)) return cookieLocale
  return localeFromAcceptLanguage(request.headers.get('accept-language'))
}

export function t(locale: Locale, key: MessageKey, vars: Record<string, string | number> = {}) {
  let message = dictionaries[locale][key] ?? dictionaries.en[key]
  for (const [name, value] of Object.entries(vars)) {
    message = message.replaceAll(`{${name}}`, String(value))
  }
  return message
}

export function createTranslator(locale: Locale) {
  return (key: MessageKey, vars?: Record<string, string | number>) => t(locale, key, vars)
}
